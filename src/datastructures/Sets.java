package datastructures;

import java.util.HashSet;

public class Sets {

	public static void main(String[] args) {
		HashSet<String> animals = new HashSet<String>();
		
		animals.add("dog");
		animals.add("cat");
		animals.add("horse");
		animals.add("pig");
		animals.add("wolf");
		animals.add("bear");
		
		System.out.println(animals);

	}

}
